// clang-format on

void rhruiz_update_layer_colors(layer_state_t state) {
    if (state & (1 << _NUM)) {
        writePinLow(D5);
        writePinLow(B0);
    } else {
        writePinHigh(D5);
        writePinHigh(B0);
    }
}
